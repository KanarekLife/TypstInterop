    [Fact]
    public void Compile_WithCustomFont_Succeeds()
    {
        using var compiler = new TypstCompiler(new TypstCompilerOptions
        {
            FontsSource = TypstFontsSource.ProvidedOnly
        });

        // "Pixel" is the name of the font in the provided TTF data
        var result = compiler.Compile(c => c
            .WithFont(PixelFont)
            .WithSource("#set text(font: \"Pixel\")\nHello in Pixel!"));

        Assert.True(result.IsSuccess, result.ErrorMessage);
        Assert.NotNull(result.GetBytes());
    }

    [Fact]
    public void Compile_WithoutRequiredCustomFont_Fails()
    {
        using var compiler = new TypstCompiler(new TypstCompilerOptions
        {
            FontsSource = TypstFontsSource.ProvidedOnly
        });

        // Should fail because "Pixel" font is not provided
        var result = compiler.Compile(c => c
            .WithSource("#set text(font: \"Pixel\")\nHello?"));

        // Typst might not "fail" compilation if font is missing (it uses fallback),
        // but in ProvidedOnly mode with NO fonts, it should probably have issues or at least
        // we can check if it behaves as expected.
        // Actually, Typst often warns but succeeds with fallbacks if ANY font is available.
        // If NO fonts are available, it might fail.
        
        // Let's check the behavior.
        if (!result.IsSuccess)
        {
            Assert.Contains("font", result.ErrorMessage?.ToLower() ?? "");
        }
    }
